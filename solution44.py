#44.Write a Python program to slice a tuple1.
tuple = ('p', 'y', 't', 'h', 'o', 'n', 1, 2, 3, 4, 5)
print(tuple)
print(tuple[:])#this will print all item of tuple1.
print(tuple[:6])#this will print up to index 6.
print(tuple[6:])#this will print from index 6 to last item
print(tuple[:-5])#this will print from back index to front index up to range 5.
print(tuple[::2])#start from 0 index with increment of 2.
print(tuple[1:5:3])#start from 1 index to 5 index with increment of 3.


